# Variables y tipos de datos

nombre = "José Varela"  #Tipo de dato str (String)
edad = 22   #Tipo de dato int (Integer)
altura = 1.8    #Tipo de dato float (Floats)
es_mexicano = True  #Tipo de dato bool (Booleans)

# Imprimiendo las variables
# El metodo moderno para imprimir texo y variables se llama "f-string"
# Simplemente se pone una f antes de las comillas.

print(f"Nombre: {nombre}")
print(f"Edad: {edad}")
print(f"Altura: {altura}")
print(f"Es mexicano? {es_mexicano}")

# También se pueden combinar

print(f"Mi nombre es {nombre}, tengo {edad} años y mi estatura es {altura} metros.")